# Boolean and Logical Operations

# Boolean values
a = True
b = False

print("a:", a)
print("b:", b)

# Logical AND
print("a AND b:", a and b)

# Logical OR
print("a OR b:", a or b)

# Logical NOT
print("NOT a:", not a)
print("NOT b:", not b)

# Comparison operators
x = 10
y = 5

print("x > y:", x > y)
print("x < y:", x < y)
print("x == y:", x == y)
print("x != y:", x != y)
print("x >= y:", x >= y)
print("x <= y:", x <= y)
